package hongchengstudy.InnerClass.v2;

public class Tank {
    private int X;
    private int Y;

    public Tank(int x, int y) {
        X = x;
        Y = y;
    }

    public Tank() {
    }

    public int getX() {
        return X;
    }

    public void setX(int x) {
        X = x;
    }

    public int getY() {
        return Y;
    }

    public void setY(int y) {
        Y = y;
    }
}
